void processADCValues(void)
{
    V1 = 0;
    for (i = Ng; i < Ng + Ns; i++) {
        V1 += adcVal[i];
    }

    V2 = 0;
    for (l = Ng + Ns + r; l < Ng + 2 * Ns + r; l++) {
        V2 += adcVal[l];
    }
    SLO = (V1 - V2) / 16;
    
    V3 = 0;
    for (j = 3 * Ng + 2 * Ns + r; j < 3 * Ng + 3 * Ns + r; j++) {
        V3 += adcVal[j];
    }

    V4 = 0;
    for (m = 3 * Ng + 3 * Ns + 2 * r; m < 3 * Ng + 4 * Ns + 2 * r; m++) {
        V4 += adcVal[m];
    }
    SRO = (V4 - V3) / 16;
    Cplatedec1 = KO / (33 * (SLO + SRO));
}

void constructXbeeFrame(uint16_t adcValue)
{
    xbeeFrame[0] = (sensorID << 4) | ((adcValue >> 8) & 0x0F);
    xbeeFrame[1] = adcValue & 0xFF;
}

